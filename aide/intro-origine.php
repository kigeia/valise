<p>
	Nous sommes nombreux à réfléchir chacun de notre côté à l'évaluation par compétences. Mais c'est une démarche qui se révèle complexe et chronophage sans l'aide d'un outil informatique adapté.<br />
	Dans <a class="lien_ext" href="http://www.college-guitres.com">mon collège</a>, afin d'évaluer par compétences (en utilisant <a class="lien_ext" href="http://michel.vauquois.free.fr/indexPL.html">des points dits «&nbsp;Lomer&nbsp;»</a>), nous avions besoin d'une interface qui permette :
</p>

<ul class="puce">
	<li>de gérer des classes, groupes, élèves, professeurs, matières...</li>
	<li>d'établir des référentiels de compétences par matière et par niveau</li>
	<li>d'imprimer des grilles de compétences à distribuer aux élèves</li>
	<li>de créer des évaluations associées à des items et d'en saisir les résultats facilement</li>
	<li>de générer des bilans d'acquisitions chiffrés pour les besoins de l'institution</li>
	<li>d'avoir un accès élèves / parents permettant de consulter les bilans de compétences</li>
</ul>

<p>
	Ne trouvant pas de logiciel simple d'utilisation et immédiatement utilisable en tant que tel, je me suis lancé au courant de l'été 2009 dans la conception d'une web-application qui corresponde à ces besoins.<br />
	Fin août 2009, j'ai présenté le début de ce travail lors de l'AG de <a class="lien_ext" href="http://www.sesamath.net">l'association Sésamath</a>, dont je suis membre depuis plusieurs années. Ce projet personnel est alors devenu <em>SACoche</em>, un projet de <img alt="Sésamath_logo" src="./_img/logo_sesamath.png" />.<br />
</p>

<p>
	Notre philosophie est que le résultat du travail de chacun est partagé afin de pouvoir bénéficier à tous. Ainsi, dans la précipitation de la rentrée, l'application fut mise en ligne sur Internet, et chacun peut depuis l'utiliser gratuitement.
</p>

