<p>
	Il est notamment prévu au courant de l'année 2010 que <em>SACoche</em> doive être téléchargé et installé par les établissements qui souhaitent l'utiliser. En effet c'est la seule solution pour :
</p>

<ul class="puce">
	<li>éviter une surcharge de la base actuelle (1 million de notes prévues la première année)</li>
	<li>rendre les établissements indépendants et responsables de leurs données</li>
</ul>

<p>
	Le code sera alors placé sous licence libre. Cela signifie aussi un gros travail : mettre en place une procédure d'installation et la documentation associée, mettre en place un système pour les mises à jour, permettre l'exportation et le rapatriement des données pour ceux qui ont démarré sur le serveur actuel, trouver une solution au partage décentralisé des référentiels...
</p>

