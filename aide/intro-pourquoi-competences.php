<p>
	Un élève, son professeur et sa famille, savent à tout moment ce que l'élève a acquis et ce qu'il maitrise moins ou pas du tout. Si on associe un lien de remédiation à chaque item, l'élève a alors la possibilité de retravailler ses points faibles, voir de réviser en amont. Un "10/20" sur une copie n'apporte pas cet éclairage et ne permet pas cette remédiation.
</p>

<p>
	L'évaluation par compétences permet une reconnaissance de l'école en indiquant à l'élève, même en difficulté, ce qu'il sait faire : c'est important pour la confiance en soi, l'estime de soi, et le respect de soi. Un "3/20" sur une copie ne renvoie pas cette image.
</p>

<p>
	L'<b>évaluation</b> par compétences permet d'estimer les acquisitions sur la durée, en favorisant les évaluations récentes : elle donne à l'élève le droit à l'erreur, elle valorise une remédiation réussie, tout en remettant aussi en question les acquis (rien n'est figé !). C'est différent d'un système de <b>validation</b> du B2i tel que pratiqué avec Gibii...
</p>

<p>
	L'évaluation par compétences permet à l'élève et sa famille d'avoir explicitement connaissance des notions travaillées, de savoir quelles sont les attentes de l'école.
</p>

