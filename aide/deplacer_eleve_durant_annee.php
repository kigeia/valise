<h2>Problématique</h2>
<p>
	Un élève a déjà été évalué par ses professeurs ; si on le change de classe ou de groupe, que va-t-il se passer concernant ses évaluations et son bilan ?
</p>

<h2>Réponse</h2>
<p>
	Quand on change un élève de classe, ses résultats antérieurs vont bien le suivre, ils resteront enregistrés, et seront intégrés à son bilan.<br />
	Mais ils ne pourront plus être modifiés, ni par le professeur qui l'avait avant, ni par celui qui l'aura ensuite.<br />
</p>
